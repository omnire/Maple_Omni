// 직업 호환성 확인 함수
window.isItemCompatible = function(itemName, characterClass) {
    // 엠블렘, 무기 등 직업 전용 장비 필터링
    if (itemName.includes("미트라의 분노")) return itemName.includes(characterClass);
    if (itemName.includes("앱솔랩스") || itemName.includes("아케인셰이드") || itemName.includes("에테르넬") || itemName.includes("하이네스") || itemName.includes("이글아이") || itemName.includes("트릭스터")) {
        const keywords = [];
        for (let job in window.JOB_KEYWORD_MAP) {
            if (characterClass.includes(job)) keywords.push(...window.JOB_KEYWORD_MAP[job]);
        }
        // 공용 아이템이 아닌 경우 해당 직업 키워드가 포함되어야 함
        if (keywords.length > 0 && !keywords.some(k => itemName.includes(k))) return false;
    }
    return true;
};