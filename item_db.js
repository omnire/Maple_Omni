/**
 * 아이콘 경로 해석기
 * 1. ITEM_REGISTRY에 등록된 이름이면 'icon/...' 경로를 반환합니다.
 * 2. assets/로 시작하면 절대 경로로 간주합니다.
 * 3. 매칭되는 이름이 없으면 기본 경로(defaultPath)를 반환합니다.
 */
window.getIconPath = function(itemName, defaultPath) {
    // [수정] 폴더 경로(value) + / + 장비이름(key) + .png 로 조합
    if (window.ITEM_REGISTRY[itemName]) {
        return window.ITEM_REGISTRY[itemName] + "/" + itemName + ".png";
    }
    return defaultPath;
};

